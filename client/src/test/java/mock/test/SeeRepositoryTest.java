package mock.test;

import static org.junit.Assert.*;

import java.util.ArrayList;
import java.util.List;

import org.junit.Test;

import businessLogic.businessLogicController.repositoryController.ManageRepositoryController;
import businessLogicService.repositoryBLService.ManageRepositoryBLService;
import constant.AreaCodeType;
import mock.object.MockSeeRepository;
import po.repositoryPO.RepositoryInfoPO;
import vo.repositoryVO.RepositoryInfoVO;

public class SeeRepositoryTest {
		MockSeeRepository mockSeeRepository = new MockSeeRepository();
		ManageRepositoryBLService manageRepository = new ManageRepositoryController();
		@Test
		public void test() {
			List<RepositoryInfoPO> repositoryInfoPOs =new ArrayList<RepositoryInfoPO>();
			repositoryInfoPOs.add(new RepositoryInfoPO("1232409000",AreaCodeType.PLANEAREA, "014","101","067", true));
			repositoryInfoPOs.add(new RepositoryInfoPO("1232409120",AreaCodeType.PLANEAREA, "015","111","167", false));
			repositoryInfoPOs.add(new RepositoryInfoPO("1232409010",AreaCodeType.TRAINAREA, "024","001","067", true));
			repositoryInfoPOs.add(new RepositoryInfoPO("1682409010",AreaCodeType.TRUCKAREA, "064","041","014", false));
			mockSeeRepository.setRepositoryDT(repositoryInfoPOs);
			List<RepositoryInfoVO> list = new ArrayList<RepositoryInfoVO>();
			list =manageRepository.SeeRepositoryBL("2015-11-7");
			for(int i=0;i<list.size();i++){
				assertEquals(repositoryInfoPOs.get(i).getdeliveryid(), list.get(i).getdeliveryid());
				assertEquals(repositoryInfoPOs.get(i).getbeinrepository(), list.get(i).getbeinrepository());
				assertEquals(repositoryInfoPOs.get(i).getareaCode(), list.get(i).getareaCode());
				assertEquals(repositoryInfoPOs.get(i).getposid(), list.get(i).getposid());
				assertEquals(repositoryInfoPOs.get(i).getrowid(), list.get(i).getrowid());
				assertEquals(repositoryInfoPOs.get(i).getshelfid(), list.get(i).getshelfid());
			}
		}

}
